public class demo6 {
    public static void main(String args[])
    {
       //LOICAL OPERATOR
       boolean Briyani = false;
       boolean Chicken = false;
       if(Briyani && Chicken)
       {
        System.out.println("I will eat Briyani and Chicken");
       }
       else if(Briyani || Chicken)
       {
        System.out.println("I will eat either Briyani or Chicken");
       }
       else
       {
        System.out.println("I will not eat Briyani and Chicken");
       }
    }
}
